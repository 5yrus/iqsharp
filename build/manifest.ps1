<#
    .SYNOPSIS
        Provides the list of artifacts (Packages and Assemblies) generated by this repository.
    
    .PARAMETER ValidateAssemblies
        Specifies if it should validate that the expected Assemblies exist.

    .PARAMETER ValidatePackages
        Specifies if it should validate that the expected packages exist.
#>

param(
    [bool] $ValidateAssemblies = $True,
    [bool] $ValidatePackages = $True
);

& "$PSScriptRoot/set-env.ps1"

$artifacts = @{
    Packages = @(
        "Microsoft.Quantum.IQSharp.Core",
        "Microsoft.Quantum.IQSharp.Jupyter",
        "Microsoft.Quantum.IQSharp"
    );
    Assemblies = @(
        "./src/Tool/bin/$Env:BUILD_CONFIGURATION/netcoreapp3.1/Microsoft.Quantum.IQSharp.dll",
        "./src/Tool/bin/$Env:BUILD_CONFIGURATION/netcoreapp3.1/Microsoft.Quantum.IQSharp.AzureClient.dll",
        "./src/Tool/bin/$Env:BUILD_CONFIGURATION/netcoreapp3.1/Microsoft.Quantum.IQSharp.Core.dll",
        "./src/Tool/bin/$Env:BUILD_CONFIGURATION/netcoreapp3.1/Microsoft.Quantum.IQSharp.Jupyter.dll",
        "./src/Tool/bin/$Env:BUILD_CONFIGURATION/netcoreapp3.1/Microsoft.Quantum.IQSharp.Kernel.dll",
        "./src/Tool/bin/$Env:BUILD_CONFIGURATION/netcoreapp3.1/Microsoft.Quantum.IQSharp.Web.dll"
    ) 
} 

if ($ValidatePackages -eq $True) {
    $artifacts.Packages | ForEach-Object { Get-Item (Join-Path $Env:NUGET_OUTDIR "$_.$Env:NUGET_VERSION.nupkg") };
}

if ($ValidateAssemblies -eq $True) {
    $artifacts.Assemblies | ForEach-Object { Get-Item (Join-Path $PSScriptRoot (Join-Path ".." $_)) };
}

$artifacts | Write-Output;
